package Nikhil;

public class escapesequence {
    public static void main(String[] args) {
        System.out.println("Hello there . .");
        System.out.println("Hello\tthere");//space
        System.out.println("Hello\nthere. .");//new line
        System.out.println("ab\bcd");//removes before word
        System.out.println("abc\rdef");//prints only right string
        System.out.println("123\\456");//to put single backslash in string
        System.out.println("\"I am Batman\"");//to put double quotations in a string
    }
}
